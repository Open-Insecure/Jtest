--创建种子列表
CREATE TABLE torrents(
   flag varchar(30),
	 type varchar(10),
	 title varchar(200),
   url varchar(100),
	 size varchar(100),
	 torrentUrl varchar(100),
	 time varchar(30),
	 updatetime varchar(30),
	 picUrl  varchar(100),
	 message varchar(3000),
	 temp varchar(500)
)default charset=utf8;








